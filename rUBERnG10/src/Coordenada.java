
public class Coordenada {

    int coordenadaX;
    int coordenadaY;


    public Coordenada(int unaCoordenadaX, int unaCoordenadaY){
        coordenadaX = unaCoordenadaX;
        coordenadaY = unaCoordenadaY;
    }

    public int getCoordenadaX() {
        return coordenadaX;
    }

    public int getCoordenadaY() {
        return coordenadaY;
    }
}
