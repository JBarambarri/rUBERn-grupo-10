
public class Coordenada {

    long coordenadaX;
    long coordenadaY;


    public Coordenada(long unaCoordenadaX, long unaCoordenadaY){
        coordenadaX = unaCoordenadaX;
        coordenadaY = unaCoordenadaY;
    }

    public long getCoordenadaX() {
        return coordenadaX;
    }

    public long getCoordenadaY() {
        return coordenadaY;
    }
}
