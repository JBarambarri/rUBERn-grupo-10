
public class ViajesRealizados {

    Chofer chofer;
    Viaje viaje;

    public ViajesRealizados(Chofer unChofer, Viaje unViaje){
        chofer = unChofer;
        viaje = unViaje;
    }

    public Chofer getChofer() {
        return chofer;
    }

    public Viaje getViaje() {
        return viaje;
    }
}
